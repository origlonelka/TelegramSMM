import re
import random


def spin(text: str) -> str:
    """Обрабатывает spintax в тексте.

    Примеры:
      {Привет|Здравствуйте|Добрый день} → случайный вариант
      {Как дела|Что нового}? → случайный вариант + "?"

    Поддерживает вложенность:
      {Привет {мир|друг}|Здравствуйте}
    """
    # Обрабатываем от внутренних к внешним (пока есть скобки)
    while "{" in text:
        text = re.sub(
            r"\{([^{}]+)\}",
            lambda m: random.choice(m.group(1).split("|")),
            text,
        )
    return text
